import java.util.*;

public class HashSetDemo{
	public static void main(String[] args){
		
		//create a hash set
		
		HashSet hs=new HashSet();

		//add elements to the hash set
		hs.add("F");		
		hs.add("C");
		hs.add("Q");
		hs.add("T");
		hs.add("Z");
		
				
		System.out.println(hs);			
	}
}