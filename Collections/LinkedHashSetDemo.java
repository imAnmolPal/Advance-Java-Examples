import java.util.*;

public class LinkedHashSetDemo{
	public static void main(String[] args){
		
		//create a hash set
		
		LinkedHashSet lhs=new LinkedHashSet();

		//add elements to the hash set
		lhs.add("F");		
		lhs.add("C");
		lhs.add("Q");
		lhs.add("T");
		lhs.add("Z");
		
				
		System.out.println(lhs);			
	}
}